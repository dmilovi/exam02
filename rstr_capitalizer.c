//выводим текущий символ, переходим к след
// main если передан только 1 аргумент, то просто выводим нов строку
// если их больше, то устанавливаем i = 1, что значит начинаем с аргумента, а не с имени программы
// while(i < ac) значит обрабатываем все аргументы пока не закончатся 
