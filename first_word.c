#include <unistd.h>

int main(int ac, char *av[])
{
    int i = 0;

    if(ac == 2)
    {
        while(av[1][i] == ' ' || av[1][i] == '\t') //проверяем пробелы перед первым словом
            i++;
        while(av[1][i] && (av[1][i] != ' ' && av[1][i] != '\t')) //пока строка и не равно пробелу и не равно переносу строки
        {
            write(1, &av[1][i], 1); //печатаем слово
            i++;
        }
    }
    write(1, "\n", 1);
    return(0);
}